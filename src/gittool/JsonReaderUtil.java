/**
 * Util class for reading json
 * @author jelena
 */
import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStream;
import java.io.InputStreamReader;
import java.io.Reader;
import java.net.URL;
import java.nio.charset.Charset;
import org.json.JSONArray;
import org.json.JSONException;
import org.json.JSONObject;

public class JsonReaderUtil {

    private static String readAll(Reader rd) throws IOException {
        StringBuilder sb = new StringBuilder();
        int cp;
        while ((cp = rd.read()) != -1) {
            sb.append((char) cp);
        }
        return sb.toString();
    }

    public static JSONObject readJsonFromUrl(String url) throws IOException, JSONException {
        InputStream is = new URL(url).openStream();
        try {
            BufferedReader rd = new BufferedReader(new InputStreamReader(is, Charset.forName("UTF-8")));
            String jsonText = readAll(rd);
            JSONObject json = new JSONObject(jsonText);
            return json;
        } finally {
            is.close();
        }
    }

    public static void readJsonArrayFromUrl(JSONObject object) throws IOException, JSONException {
        JSONArray arr = new JSONArray(object.getString("items"));
        for (int i = 0; i < arr.length(); i++) {

            JSONObject jsonProductObject = arr.getJSONObject(i);
            String name = jsonProductObject.getString("name");
            String id = jsonProductObject.getString("id");
            System.out.println(id + " " + name);
           
        }

    }

}
